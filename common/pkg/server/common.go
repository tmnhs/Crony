package server

import (
	"bytes"
	"fmt"
	"io/ioutil"
	"runtime"
	"time"
)

var (
	dunno     = []byte("???")
	centerDot = []byte("Â·")
	dot       = []byte(".")
	slash     = []byte("/")
)

